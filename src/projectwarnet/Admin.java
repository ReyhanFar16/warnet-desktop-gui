    public void tampilanMember() {
        DefaultTableModel tb = new DefaultTableModel();
        tb.addColumn("Username");
        tb.addColumn("Harga");
        tb.addColumn("Waktu");
        jTable_NamaMember.setModel(tb);
        
        String cari = jTextField_SearchMember.getText();
        
        try {
            String sql = "SELECT * FROM tbl_akun WHERE Username LIKE'%"+cari+"%'";
            java.sql.Connection conn = (Connection) KoneksiDataMember.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet result = pst.executeQuery();
            while (result.next()) {
                tb.addRow(new Object[] {
                result.getString("Username"),
                result.getDouble("Harga"),
                result.getTime("Waktu"),
                });
            }
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan");
        }
        
    }
    
