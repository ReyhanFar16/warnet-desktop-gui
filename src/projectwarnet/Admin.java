    public void jumlahTransaksi() {
        try {
            String selectHargaSql = "SELECT SUM(Harga) AS TotalHarga FROM tbl_transaksi";
            java.sql.Connection conn = (Connection) KoneksiDataMember.configDB();
            java.sql.PreparedStatement statement = conn.prepareStatement(selectHargaSql);
            ResultSet rs = statement.executeQuery();

            if (rs.next()) {
                double totalHarga = rs.getDouble("TotalHarga");
                String totalHargaString = String.valueOf(totalHarga);
                jLabel_HargaTransaksi.setText("Rp. "+totalHargaString);
    
            } else {
                System.out.println("Data Harga tidak ditemukan");
            }

            rs.close();
            statement.close();
        
        } catch (Exception e) {
            System.out.println("Data Gagal Ditampilkan");
        }

    }
   
    
