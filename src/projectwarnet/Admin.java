    private void jButton_GantiPassword_2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_GantiPassword_2ActionPerformed
        try {
            String username = jTextField_UsernameGantiPassword.getText();
            String passwordLama = jPasswordField_PasswordLamaGantiPassword.getText();
            String passwordBaru = jPasswordField_PasswordBaruGantiPassword.getText();

            String sql = "SELECT * FROM datamember WHERE Username = ?";
            String sql1 = "UPDATE datamember SET Password = ? WHERE Username = ?";

            java.sql.Connection conn = (Connection) KoneksiDataMember.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            java.sql.PreparedStatement pstm1 = conn.prepareStatement(sql1);

            pstm.setString(1, username);
            ResultSet resultSet = pstm.executeQuery();

            if (resultSet.next()) {
                String existingPassword = resultSet.getString("Password");
                if (existingPassword.equals(passwordLama)) {
                    pstm1.setString(1, passwordBaru);
                    pstm1.setString(2, username);
                    int rowsUpdated = pstm1.executeUpdate();

                    if (rowsUpdated > 0) {
                        JOptionPane.showMessageDialog(this, "Password Baru Berhasil Diganti");
                    } else {
                        JOptionPane.showMessageDialog(this, "Gagal Mengganti Password");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Password Lama Salah");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Username Tidak Ditemukan");
            }

            conn.close();

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }//GEN-LAST:event_jButton_GantiPassword_2ActionPerformed

